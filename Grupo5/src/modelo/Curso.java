public class Curso {
    int id_Curso;
    String idioma;
    
    public Curso(){
        
    }
    
    public Curso(int id_Curso, String idioma){
        this.id_Curso=id_Curso;
        this.idioma=idioma;
    }

    public int getId_Curso() {
        return id_Curso;
    }

    public void setId_Curso(int id_Curso) {
        this.id_Curso = id_Curso;
    }

    public String getIdioma() {
        return idioma;
    }

    public void setIdioma(String idioma) {
        this.idioma = idioma;
    }
    
}
